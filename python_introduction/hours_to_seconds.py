# This script converts a given number of hours into seconds.
# The formula for the conversion is seconds = hours * 3600

# Step 1: Define the variable for the number of hours
# The number of hours to convert
hours = 2

# Step 2: Calculate the number of seconds in the given hours
# There are 3600 seconds in one hour (60 minutes * 60 seconds)
seconds = hours * 3600

# Step 3: Print the result
# Display the result using a formatted string for clarity
print(f"{hours} hour(s) is {seconds} seconds.")

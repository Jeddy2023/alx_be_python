# This script calculates the user's age in the year 2050 based on their current age.

# Step 1: Prompt the user to input their current age
current_age = int(input("How old are you? "))

# Step 2: Calculate the user's age in 2050
# Since 2050 is 27 years from 2023, we add 27 years to the current age
age_in_2050 = current_age + 27

# Step 3: Print the user's age in 2050
print(f"In 2050, you will be {age_in_2050} years old.")

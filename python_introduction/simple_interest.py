# This script calculates the simple interest earned on an investment over a period of time.
# The formula for simple interest is I = P * R * T, where:
# P = principal (initial investment), R = annual interest rate, T = time in years

# Step 1: Define the variables with the given values

# Principal amount (initial investment in dollars)
principal = 1000

# Annual interest rate (5% represented as a decimal)
rate = 0.05

# Time the money is invested (in years)
time = 3

# Step 2: Calculate the simple interest using the formula
# I = P * R * T
interest = principal * rate * time

# Step 3: Print the calculated interest
# The result is printed in a formatted string for clarity
print(f"The simple interest is: {interest}")

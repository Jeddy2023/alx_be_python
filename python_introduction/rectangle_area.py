# This script calculates the area of a rectangle using its length and width.
# The formula for the area of a rectangle is Area = length * width

# Step 1: Define the variables for length and width
# Length of the rectangle (in units)
length = 10

# Width of the rectangle (in units)
width = 5

# Step 2: Calculate the area of the rectangle
# Area = length * width
area = length * width

# Step 3: Print the calculated area
# The result is displayed in a formatted string for clarity
print(f"The area of the rectangle is: {area}")
